    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        int index = birthdayList.getSelectedIndex();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Please select an entry to delete!");
            return;
        }

        String name = birthdayEntries.get(index).getName();
        int confirm = JOptionPane.showConfirmDialog(this,
                "Are you sure you want to delete: " + name + "?",
                "Confirm Delete",
                JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            dbService.deleteBirthday(username, name);
            JOptionPane.showMessageDialog(this, "Deleted successfully!");
            loadBirthdays();
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        int index = birthdayList.getSelectedIndex();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Please select an entry to edit!");
            return;
        }

        BirthdayEntry selectedEntry = birthdayEntries.get(index);
        new BirthdayAdd(username, selectedEntry, selectedLanguage).setVisible(true);
        this.dispose();
    }//GEN-LAST:event_editButtonActionPerformed

