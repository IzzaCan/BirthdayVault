    private void backupButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backupButtonActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        int option = fileChooser.showSaveDialog(this);

        if (option == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();

            // Ambil semua entri user saat ini
            List<BirthdayEntry> entries = dbService.getAllBirthdaysByUser(username);

            // Simpan ke file
            BirthdayDataManager.backupToFile(entries, selectedFile);

            JOptionPane.showMessageDialog(this, "Backup berhasil disimpan ke: " + selectedFile.getAbsolutePath());
        }
    }//GEN-LAST:event_backupButtonActionPerformed

    private void restoreButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_restoreButtonActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        int option = fileChooser.showOpenDialog(this);

        if (option == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();
            List<BirthdayEntry> restoredEntries = BirthdayDataManager.restoreFromFile(selectedFile);

            if (restoredEntries != null) {
                for (BirthdayEntry entry : restoredEntries) {
                    // Pastikan hanya untuk user yang sedang login
                    if (username.equals(entry.getUser())) {
                        dbService.addBirthday(entry);
                    }
                }
                JOptionPane.showMessageDialog(this, "Data berhasil di-restore dari: " + selectedFile.getAbsolutePath());
                loadBirthdays();
            } else {
                JOptionPane.showMessageDialog(this, "Gagal memuat file backup!", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_restoreButtonActionPerformed

