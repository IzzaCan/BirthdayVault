    private void loadBirthdays() {
        birthdayEntries = dbService.getAllBirthdaysByUser(username);
        listModel = new DefaultListModel<>();

        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("d MMMM yyyy", new Locale("id", "ID"));

        for (BirthdayEntry entry : birthdayEntries) {
            String dateFormatted = entry.getBirthDate().format(formatter);
            String display = entry.getName() + " | " + dateFormatted + " | " + entry.getMessage();
            listModel.addElement(display);
        }

        birthdayList.setModel(listModel);
    }

